package jp.co.sss.crud.model.beans;

public class RoseBeans {

	private int roseId;
	private String roseName;
	private int rosePrice;
	private String rosePicture;


	public int getRoseId() {
		return roseId;
	}

	public void setRoseId(int roseId) {
		this.roseId = roseId;
	}

	public String getRoseName() {
		return roseName;
	}

	public void setRoseName(String roseName) {
		this.roseName = roseName;
	}

	public int getRosePrice() {
		return rosePrice;
	}

	public void setRosePrice(int rosePrice) {
		this.rosePrice = rosePrice;
	}

	public String getRosePicture() {
		return rosePicture;
	}

	public void setRosePicture(String rosePicture) {
		this.rosePicture = rosePicture;
	}

}
